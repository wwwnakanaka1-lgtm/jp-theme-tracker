def get_theme_names() -> list[str]:
    """テーマ名一覧を取得"""
    return list(THEMES.keys())


def get_theme_by_id(theme_id: str) -> dict | None:
    """IDでテーマを取得"""
    return THEMES.get(theme_id)


def get_theme_tickers(theme_id: str) -> list[str]:
    """テーマに属する銘柄コード一覧を取得"""
    theme = THEMES.get(theme_id)
    if not theme:
        return []
    return theme["tickers"]


def get_theme_description(theme_id: str) -> str:
    """テーマの説明を取得"""
    theme = THEMES.get(theme_id)
    if not theme:
        return ""
    return theme["description"]


def get_ticker_name(theme_id: str, ticker: str) -> str:
    """銘柄コードから企業名を取得"""
    theme = THEMES.get(theme_id)
    if not theme:
        return ticker
    return theme["ticker_names"].get(ticker, ticker)


def get_all_tickers() -> list[str]:
    """全テーマの全銘柄を取得（重複なし）"""
    all_tickers = set()
    for theme_data in THEMES.values():
        all_tickers.update(theme_data["tickers"])
    return list(all_tickers)


def get_ticker_description(theme_id: str, ticker: str) -> str | None:
    """銘柄コードから銘柄説明を取得"""
    theme = THEMES.get(theme_id)
    if not theme:
        return None
    descriptions = theme.get("ticker_descriptions", {})
    return descriptions.get(ticker)


def get_ticker_info(ticker: str) -> dict | None:
    """銘柄コードから企業情報を取得（所属テーマ含む）"""
    for theme_id, theme_data in THEMES.items():
        if ticker in theme_data["tickers"]:
            descriptions = theme_data.get("ticker_descriptions", {})
            return {
                "ticker": ticker,
                "name": theme_data["ticker_names"].get(ticker, ticker),
                "description": descriptions.get(ticker),
                "theme_id": theme_id,
                "theme_name": theme_data["name"],
            }
    return None
